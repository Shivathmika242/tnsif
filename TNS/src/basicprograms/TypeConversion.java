package basicprograms;

public class TypeConversion {

	public static void main(String[] args) {
		
		 //Type Conversion(Implicit)
		
		//int -> float
		   int h = 15;
	       float g= h;
	       System.out.println(" int to float " + g);
	     
	     //  byte → int 
	        byte b = 10;
	        int i = b; 
	        System.out.println(" byte to int = " + i);
	        
	    //  short → long 
	        short s = 200;
	        long l = s;
	        System.out.println(" short to long = " + l);
	        
	      //  int → double 
	        int a = 42;
	        double d =  a;
	        System.out.println("int to double = " + d);
	        
	        //  char → int 
	        char ch = 'A'; 
	        int ascii = ch;
	        System.out.println(" char to int " + ascii);
	        
	        //  float → double
	        float f = 12.5f;
	        double d3 = f;
	        System.out.println(" float to double = " + d3);
	        
	        

	}

}
